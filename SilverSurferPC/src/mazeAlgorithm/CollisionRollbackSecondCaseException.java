package mazeAlgorithm;

@SuppressWarnings("serial")
public class CollisionRollbackSecondCaseException extends Exception {
	
	public CollisionRollbackSecondCaseException() {
		
	}

	public CollisionRollbackSecondCaseException(String message) {
		super(message);
    }

	public CollisionRollbackSecondCaseException(Throwable cause) {
		super(cause);
    }

	public CollisionRollbackSecondCaseException(String message, Throwable cause) {
		super(message, cause);
    }
}